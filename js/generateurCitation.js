	function displayReponse() {
		submitOK = "true";
		var radiosButtons = document.getElementsByName('typeCitation');
		var selectUserTypeCitation;
			for(var i = 0; i < radiosButtons.length; i++){
		 		if(radiosButtons[i].checked){
		 		selectUserTypeCitation = radiosButtons[i].value;
		 	}
		};

		var selectUserNbCitation = document.getElementById("nbCitations").value;
		
		document.getElementById('resultat_citations').innerHTML ="";
		doItCitation(selectUserTypeCitation, selectUserNbCitation).forEach(function(element){
			document.getElementById('resultat_citations').innerHTML += element;
		})
	};

