// FONCTIONS - GENERATIONS

		function getRandomInt(arrayName) {
		    return Math.floor(Math.random() * (arrayName.length - 1));
		};

		function generateCitation(option) {
		    var citations = [];
		    switch (option) {
		        case 'citationsIntelligentes':
		            citations = introAll[getRandomInt(introAll)].texte + " " + corpsAll[getRandomInt(corpsAll)].texte + " " + conclusionAll[getRandomInt(conclusionAll)].texte + '<br/><br/>';
		            break;
		        case 'citationsRetard':
		            citations = introExcuseAll[getRandomInt(introExcuseAll)].texte + " " + corpsExcuseAll[getRandomInt(corpsExcuseAll)].texte + " " + conclusionExcuseAll[getRandomInt(conclusionExcuseAll)].texte +'<br/><br/>';
		            break;
		    }
		    return citations;
		};

		function doItCitation(option, nombre) {
		    var resultcitation = [];
		    for (var i = 0; i < nombre; i++) {
		        resultcitation.push(generateCitation(option));
		    }
		    return resultcitation;
		};
	
